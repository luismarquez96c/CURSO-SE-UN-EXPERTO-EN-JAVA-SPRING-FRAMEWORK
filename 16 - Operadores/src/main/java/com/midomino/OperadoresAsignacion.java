package com.midomino;

public class OperadoresAsignacion {

	public static void main(String[] args) {
		// CLASE 17: OPERADORES DE ASIGNACION

		// operador =
		String cadena = "mi cadena";
		
		int edad = 24;
		
		// operador +=
		edad += 3; // edad = edad + 3;
		
		System.out.println("edad: " + edad);
		
		// operador -=
		edad -= 4; // edad = edad - 4;
		
		System.out.println("edad: " + edad);
		
		// operador *=
		edad *= 3; // edad = edad * 3;
		
		System.out.println("edad: " + edad);
		
		// operador /=
		edad /= 4; // edad = edad / 3;
		
		System.out.println("edad: " + edad);
		
	}

}
